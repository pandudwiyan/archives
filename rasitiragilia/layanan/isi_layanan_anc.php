        <div class="detail-sec" style="margin-top: 30px;">
            <p style="text-align: justify; text-indent: 50px;">Pemeriksaan ANC (Antenatal Care) merupakan pemeriksaan kehamilan yang bertujuan untuk meningkatkan kesehatan fisik dan mental pada ibu hamil secara optimal, hingga mampu menghadapi masa persalinan, nifas, menghadapi persiapan pemberian ASI secara eksklusif, serta kembalinya kesehatan alat reproduksi dengan wajar. Pemeriksaan kehamilan dilakukan minimal 4 (empat) kali selama masa kehamilan, yaitu 1 kali pemeriksaan pada trimester pertama, 1 kali pemeriksaan pada trimester kedua, dan 2 kali pemeriksaan pada trimester ketiga.</p><br>

            <p>Tujuan dari ANC (Antenatal Care) adalah untuk:</p>
            <p>- Memantau kemajuan proses kehamilan demi memastikan kesehatan pada ibu serta tumbuh kembang janin yang ada di dalamnya.<br>
                - Mengetahui adanya komplikasi kehamilan yang mungkin saja terjadi saat kehamilan sejak dini, termasuk adanya riwayat penyakitdan tindak pembedahan. <br>
                - Meningkatkan serta mempertahankan kesehatan ibu dan bayi.<br>
                - Mempersiapkan proses persalinan sehingga dapat melahirkan bayi dengan selamat serta meminimalkan trauma yang dimungkinkan terjadi pada masa persalinan.<br>
                - Menurunkan jumlah kematian dan angka kesakitan pada ibu.<br>
                - Mempersiapkan peran sang ibu dan keluarga untuk menerima kelahiran anak agar mengalami tumbuh kembang dengan normal.<br>
                - Mempersiapkan ibu untuk melewati masa nifas dengan baik serta dapat memberikan ASI eksklusif pada bayinya.<br></p><br>
            <p style="text-align: justify;">PMB <?= $author ?> menyediakan layanan Pemeriksaan ANC (Antenatal Care) yang bisa anda gunakan. Anda bisa berkonsultasi terlebih dahulu dengan membuat janji melalui kontak yang tersedia atau mengunjungi secara langsung ke alamat kami.</p>
