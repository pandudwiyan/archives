            <p style="text-align: justify; text-indent: 50px;">Keluarga berencana (disingkat KB) adalah gerakan untuk membentuk keluarga yang sehat dan sejahtera dengan membatasi kelahiran hal itu bermaksud untuk merencankan jumlah keluarga dengan pembatasan kelahiran yang dilakukan menggunakan alat-alat kontrasepsi atau penanggulangan kelahiran seperti kondom, spiral, IUD, dan sebagainya. Gerakan keluarga berencana diartikan sebagai upaya peningkatan kepedulian dan peran serta masyarakat melalui upaya pendewasaan usia perkawinan, pengendalian kelahiran, pembinaan ketahanan keluarga, dan peningkatan kesejahteraan keluarga dalam rangka melembagakan dan membudidayakan norma keluarga kecil bahagia dan sejahtera.</p><br>

            <p style="text-align: justify;">Berbagai jenis alat kontrasepsi yang dapat digunakan untuk mendukung gerakan KB diantaranya:<br>
                - Pil KB<br>
                - Kondom Pria<br>
                - Suntik KB<br>
                - Implan <br>
                - IUD <br>
                - Kondom Wanita <bt>
                - Spermisida <br>
                - Diafragma<br>
                - Cervical Cap<br>
                - Koyo Ortho Evra<br>
                - Cincin Vagina <br>
                - KB Permanent<br>
            </p><br>

